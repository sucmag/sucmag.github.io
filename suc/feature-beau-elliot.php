<p>“I think you find out a lot about yourself when you’re trying to force things,” he added.</p>

<p>Beau said he has a lot goals, a few being a clothing line, comics, and a children’s book.</p>

<p>“I’m in a weird phase in my work,” Beau said. “There are so many things I want out of it and so many things I struggle to get.”</p>

<p>Until then, Beau says he continues to navigate the frustrating world of self-promoting his art on social media.</p>

<p>He said it’s an uncomfortable form of exposure.</p>

<p>“It’s really unpleasant because you’re constanting getting ratings for everything you do,” Beau said. “At it’s core, it’s a good thing. But it’s intimidating. Social media is such an intense part of our society these days.”</p>

<p>The ease of access social media offers is also a good thing, especially when artists are looking for inspiration or seeking other creatives to hang around.</p>

<p>“A lot of my friends do art, and I think that’s a great community to be in,” Beau said.</p>

