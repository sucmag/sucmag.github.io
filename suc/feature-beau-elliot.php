<p>Beau’s art depicts anxious, ugly characters contorted in vibrant scenes created to make its viewer uncomfortable, yet the overall pieces come together in whimsical imperfection. His sketchbooks are filled with crying faces in panels surrounded by bright colors and graceful hands reaching out toward something. It’s like Alex Pardee meets “Adventure Time.”</p>

<p>Although that imperfection is part of the draw (no pun intended) to Beau’s nonsensical illustrations that fall somewhere between uncomfortable and graceful, it’s something he grapples with on nearly every piece.</p>

<p>“There’s something that feels very authentic about it,” Beau said. “It’s hard for me to leave mistakes even when I want them to be there. It’s a bad mixture.”</p>

<p>Perhaps one solution, Beau said, has been creating digital illustrations. As a recent graduate of Dixie State University’s film program, he has both the practical training in storytelling, and he often uses storyboards to communicate in his artwork.</p>

<p>But while illustrating by hand is somewhat permanent, Beau, a St. George native, says working digitally is both a blessing and a curse--a blessing because he’s able to achieve a clean, polished look that favorable design-wise, but a curse because he often finds himself returning to the same pieces, recoloring them and altering them in circles.</p>

<div class="inline-img">
 <div class="inline-img-caption">
   <h5>"Forest Head" by Beau Elliot</h5>
 </div>
 <div class="inline-img-content">
<img class="inline-img-control" src="assets/images/forest_head.png">
</div>
</div>

<p>“Sometimes I go back to the same things over and over again,” he said. “It freaks me out.”</p>

<p>The 23-year-old said he appreciates when someone calls his work “weird,” because he knows it breaks some perceived norms in the illustration world.</p>

<p>Beau’s style is both ugly and pretty at the same time. It’s high-energy, and sometimes it’s a manifestation of his own feelings, but other times it’s not, he said.</p>

<p>“I found that I love the energy and anxiety,” Beau said. “It’s like having a lot of ambition but not knowing exactly where to put that and sometimes thinking the places you’ve put it are ineffective.”</p>

<p>However, most of the time, Beau said assigning his ambitions to art is always an effective route, even if it doesn’t immediately seem that way.</p>

<p>It’s for that reason why Beau said he typically posts everything he creates, even if it’s a piece he doesn’t like too much. He said anxious art typically lends itself to more pointed art.</p>

<p>His process, he said, is literally forcing himself to draw at least something every day. And if he doesn’t feel inspired? He still forces it.</p>

