     <p>
       With the soft, melodic sounds of obscure indie-rock playing in the background, Beau Elliott lay on his bedroom floor, felt-tipped markers in hand and mouth.</p>

<p>He’s crouching over a drawing, and, more often than not, it’s wildly different than the physical space around him.</p>

